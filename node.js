// Simple JavaScript Program

// Function to greet a user
function greet(name) {
    return `Hello, ${name}! Welcome to JavaScript.`;
}

// Add two numbers
function add(a, b) {
    return a + b;
}

// Example usage
let userName = "atul";
console.log(greet(userName)); // Prints greeting
console.log("Sum of 5 and 10 is:", add(5, 10)); // Prints sum
